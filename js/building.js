
/* Shows Paragraph and sets opacity to either 0.5 or 1 on mouseover and mouseout */

function show(id, id2){
  document.getElementById(id).style.display = "block";
  document.getElementById(id2).style.opacity = "0.5";
}


function hide(id, id2){
  document.getElementById(id).style.display = "none";
    document.getElementById(id2).style.opacity = "1";
}




